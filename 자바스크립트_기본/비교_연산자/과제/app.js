/* 결과 예측 */
console.log(5 > 4); // true
console.log('apple' > 'pineapple'); //false
console.log('2' > '12'); // true 여기도 문자열로서 비교해야함!!
console.log(undefined == null); // true
console.log(undefined === null); // false
console.log(null == '\n0\n'); // false null은 항상 undefined하고만 같다!!
console.log(null === +'\n0\n'); // false 형이 다르다!
